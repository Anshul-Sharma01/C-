#include <iostream>
using namespace std;
int main(){
    int n;
    cin>>n;
    char ch='A';
    for(int i=0;i<n;i++){
        int j=1,k=0;
        for(;j<=n-1-i;j++){
            cout<<" ";
        }
        k=j;
        for(;k<=n;k++){
            cout<<ch;
        }
        cout<<endl;
        ch++;
        
    }
    return 0;
}